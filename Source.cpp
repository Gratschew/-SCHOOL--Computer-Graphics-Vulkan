	float randomFloat(float min, float max)
	{
		std::random_device rd;  // Will be used to obtain a seed for the random number engine
		std::mt19937 gen(rd()); // Standard mersenne_twister_engine seeded with rd()
		std::uniform_real_distribution<> dis(min, max);
		return dis(gen);
	}

	void updateUniformBuffer(uint32_t currentImage) {
	
#ifdef MODEL
		static auto startTime = std::chrono::high_resolution_clock::now();

		auto currentTime = std::chrono::high_resolution_clock::now();
		float time = std::chrono::duration<float, std::chrono::seconds::period>(currentTime - startTime).count();

		UniformBufferObject ubo{};
		ubo.model = glm::rotate(glm::mat4(1.0f), time * glm::radians(90.0f), glm::vec3(0.0f, 1.0f, 0.0f));

		// Set the camera so the 3D model can be better seen
		ubo.view = glm::lookAt(glm::vec3(10.0f, 10.0f, 13.0f), glm::vec3(0.0f, 2.0f, 0.0f), glm::vec3(0.0f, 1.0f, 0.0f));
		ubo.proj = glm::perspective(glm::radians(60.0f), swapChainExtent.width / (float)swapChainExtent.height, 0.1f, 100.0f);
		ubo.proj[1][1] *= -1;

		void* data;
		vkMapMemory(device, uniformBuffersMemory[currentImage], 0, sizeof(ubo), 0, &data);
		memcpy(data, &ubo, sizeof(ubo));
		vkUnmapMemory(device, uniformBuffersMemory[currentImage]);
#else
		// Assign random floats for x and y between -0.5f and 0.5f
		static float x = randomFloat(-0.5f, 0.5f);
		static float y = randomFloat(-0.5f, 0.5f);
		static auto startTime = std::chrono::high_resolution_clock::now();

		// Multiplier to lower speed
		static auto timeConst = 0.4;

		auto currentTime = std::chrono::high_resolution_clock::now();
		float time = std::chrono::duration<float, std::chrono::seconds::period>(currentTime - startTime).count();


		UniformBufferObject ubo{};

		// Position is the current position of the object. xfront and y front are the multipliers (+ or -) for the direction of movement.
		glm::vec3 position = glm::vec3(xfront * timeConst * time + x, yfront * timeConst * time + y, 0.0f);

		// Make the model translate to a position
		ubo.model = glm::translate(glm::mat4(1.0f), position);

		// Set an orthogonal camera
		ubo.view = glm::lookAt(glm::vec3(0.0f, 0.0f, 1.0f), glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f, -1.0f, 0.0f));
		ubo.proj = glm::ortho(-1, 1, -1, 1);

		// Surface normals for X and Y bounds
		glm::vec3 x1_normal = glm::vec3(0.0, 1.0f, 0.0f);
		glm::vec3 x2_normal = glm::vec3(0.0, -1.0f, 0.0f);
		glm::vec3 y1_normal = glm::vec3(1.0, 0.0f, 0.0f);
		glm::vec3 y2_normal = glm::vec3(-1.0, 0.0f, 0.0f);

		// Vector for reflected vector's position info
		glm::vec3 reflected;

		ubo.proj[1][1] *= -1;

		glm::vec4 h_pos = ubo.proj * ubo.view * ubo.model * glm::vec4(position, 1.0);

		// If model's x coordinate is over view's bounds, it's direction will be changed

		// x1
		if (h_pos.x < -h_pos.w) {
			reflected = glm::reflect(glm::vec3(x, y, 0.0f), x1_normal);

			if (reflected.x < position.x) {
				xfront = -1.0f;
			}
			else {
				xfront = 1.0f;
			}
			if (reflected.y < position.y) {
				yfront = -1.0f;
			}
			else {
				yfront = 1.0f;
			}

			x = position.x;
			y = position.y;
			startTime = std::chrono::high_resolution_clock::now();
		}

		// x2
		if (h_pos.x > h_pos.w) {
			reflected = glm::reflect(glm::vec3(x, y, 0.0f), x2_normal);

			if (reflected.x < position.x) {
				xfront = -1.0f;
			}
			else {
				xfront = 1.0f;
			}
			if (reflected.y < position.y) {
				yfront = -1.0f;
			}
			else {
				yfront = 1.0f;
			}

			x = position.x;
			y = position.y;
			startTime = std::chrono::high_resolution_clock::now();
		}

		// y1
		if (h_pos.y < -h_pos.w) {
			reflected = glm::reflect(glm::vec3(x, y, 0.0f), y1_normal);


			if (reflected.x < position.x) {
				xfront = -1.0f;
			}
			else {
				xfront = 1.0f;
			}
			if (reflected.y < position.y) {
				yfront = -1.0f;
			}
			else {
				yfront = 1.0f;
			}

			x = position.x;
			y = position.y;
			startTime = std::chrono::high_resolution_clock::now();
		}

		// y2
		if (h_pos.y > h_pos.w) {
			reflected = glm::reflect(glm::vec3(x, y, 0.0f), y2_normal);


			if (reflected.x < position.x) {
				xfront = -1.0f;
			}
			else {
				xfront = 1.0f;
			}
			if (reflected.y < position.y) {
				yfront = -1.0f;
			}
			else {
				yfront = 1.0f;
			}

			x = position.x;
			y = position.y;
			startTime = std::chrono::high_resolution_clock::now();
		}


		// Copy the data in the uniform buffer object to the current uniform buffer.
		void* data;
		vkMapMemory(device, uniformBuffersMemory[currentImage], 0, sizeof(ubo), 0, &data);
		memcpy(data, &ubo, sizeof(ubo));
		vkUnmapMemory(device, uniformBuffersMemory[currentImage]);
#endif

	}

	void recreateSwapChain() {
		int width = 0, height = 0;
		while (width == 0 || height == 0)
		{
			glfwGetFramebufferSize(window, &width, &height);
			glfwWaitEvents();
		}
		vkDeviceWaitIdle(device);

		cleanupSwapChain();
		createSwapChain();
		createImageViews();
		createRenderPass();
		createGraphicsPipeline();

#ifdef MODEL
		createDepthResources();
#else

#endif
		createFramebuffers();
		createUniformBuffers();
		createDescriptorPool();
		createDescriptorSets();
		createCommandBuffers();
	}


	void cleanupSwapChain() {
		for (auto framebuffer : swapChainFramebuffers) {
		vkDestroyFramebuffer(device, framebuffer, nullptr);
		}

		vkFreeCommandBuffers(device, commandPool, static_cast<uint32_t>(commandBuffers.size()), commandBuffers.data());

		vkDestroyPipeline(device, graphicsPipeline, nullptr);
		vkDestroyPipelineLayout(device, pipelineLayout, nullptr);
		vkDestroyRenderPass(device, renderPass, nullptr);

		for (auto imageView : swapChainImageViews) {
		vkDestroyImageView(device, imageView, nullptr);
		}

		vkDestroySwapchainKHR(device, swapChain, nullptr);

		for (size_t i = 0; i < swapChainImages.size(); i++) {
			vkDestroyBuffer(device, uniformBuffers[i], nullptr);
			vkFreeMemory(device, uniformBuffersMemory[i], nullptr);
		}

		vkDestroyDescriptorPool(device, descriptorPool, nullptr);
	
	}

	void drawFrame() {
		vkWaitForFences(device, 1, &inFlightFences[currentFrame], VK_TRUE, UINT64_MAX);
		uint32_t imageIndex;

		VkResult result = vkAcquireNextImageKHR(device, swapChain, UINT64_MAX, imageAvailableSemaphores[currentFrame], VK_NULL_HANDLE, &imageIndex);
		
		if (result == VK_ERROR_OUT_OF_DATE_KHR) {
			recreateSwapChain();
			return;
		}
		else if (result != VK_SUCCESS && result != VK_SUBOPTIMAL_KHR) {
			throw std::runtime_error("failed to acquire swap chain image!");
		}

		
		// Check if a previous frame is using this image (i.e. there is its fence to wait on)
		if (imagesInFlight[imageIndex] != VK_NULL_HANDLE) {
			vkWaitForFences(device, 1, &imagesInFlight[imageIndex], VK_TRUE, UINT64_MAX);
		}
		// Mark the image as now being in use by this frame
		imagesInFlight[imageIndex] = inFlightFences[currentFrame];

		updateUniformBuffer(imageIndex);

		VkSubmitInfo submitInfo{};
		submitInfo.sType = VK_STRUCTURE_TYPE_SUBMIT_INFO;

		VkSemaphore waitSemaphores[] = { imageAvailableSemaphores[currentFrame]};
		VkPipelineStageFlags waitStages[] = { VK_PIPELINE_STAGE_COLOR_ATTACHMENT_OUTPUT_BIT };
		submitInfo.waitSemaphoreCount = 1;
		submitInfo.pWaitSemaphores = waitSemaphores;
		submitInfo.pWaitDstStageMask = waitStages;

		submitInfo.commandBufferCount = 1;
		submitInfo.pCommandBuffers = &commandBuffers[imageIndex];

		VkSemaphore signalSemaphores[] = { renderFinishedSemaphores[currentFrame] };
		submitInfo.signalSemaphoreCount = 1;
		submitInfo.pSignalSemaphores = signalSemaphores;

		vkResetFences(device, 1, &inFlightFences[currentFrame]);

		if (vkQueueSubmit(graphicsQueue, 1, &submitInfo, inFlightFences[currentFrame]) != VK_SUCCESS) {
			throw std::runtime_error("failed to submit draw command buffer!");
		}

		VkPresentInfoKHR presentInfo{};
		presentInfo.sType = VK_STRUCTURE_TYPE_PRESENT_INFO_KHR;

		presentInfo.waitSemaphoreCount = 1;
		presentInfo.pWaitSemaphores = signalSemaphores;

		VkSwapchainKHR swapChains[] = { swapChain };
		presentInfo.swapchainCount = 1;
		presentInfo.pSwapchains = swapChains;
		presentInfo.pImageIndices = &imageIndex;
		presentInfo.pResults = nullptr; // Optional

		result = vkQueuePresentKHR(presentQueue, &presentInfo);

		if (result == VK_ERROR_OUT_OF_DATE_KHR || result == VK_SUBOPTIMAL_KHR || framebufferResized)
		{
			framebufferResized = false;
			recreateSwapChain();
		}
		else if (result != VK_SUCCESS)
		{
			throw std::runtime_error("Failed to present swap chain image!");
		}

		currentFrame = (currentFrame + 1) % MAX_FRAMES_IN_FLIGHT;
	}

	VkShaderModule createShaderModule(const std::vector<char>& code) {
		VkShaderModuleCreateInfo createInfo{};
		createInfo.sType = VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO;
		createInfo.codeSize = code.size();
		createInfo.pCode = reinterpret_cast<const uint32_t*>(code.data());

		VkShaderModule shaderModule;
		if (vkCreateShaderModule(device, &createInfo, nullptr, &shaderModule) != VK_SUCCESS) {
			throw std::runtime_error("failed to create shader module!");
		}

		return shaderModule;
	}


	static std::vector<char> readFile(const std::string& filename) {
		std::ifstream file(filename, std::ios::ate | std::ios::binary);

		if (!file.is_open()) {
			throw std::runtime_error("failed to open file!");
		}

		size_t fileSize = (size_t)file.tellg();
		std::vector<char> buffer(fileSize);

		file.seekg(0);
		file.read(buffer.data(), fileSize);

		file.close();

		return buffer;
	}

