/*
HIGH LEVEL DESCRIPTION OF WHAT THE PROGRAM DOES IN ORDER TO RENDER THE TRIANGLE:
First Vulkan is initialized by creating an instance. Instance works as a bridge between the Vulkan API and our application. Instance contains information such as app's name, extensions, things that are not default in Vulkan. Instance is also used to
select the physical devise also known as the graphic card(s) of the system. Then a logical device represents the physical device, which is initialized and used to create resources and queues. Window surface is initialized. 
Physical device gives access to queue families, which are queues with the same properties to eachother. Commands are given to queues by feeding the commands into a command buffer and then submitting the commandbuffer as a whole to a queue. These commands can 
represent actions, state settings and synchronization. Synchronization is important in order for certain commands to occur after other commands have finished executing. Then comes the render pass, which is used to execute commands like drawing. 
A render pass consists of framebuffers which are like memory attachments that the render pass' subpasses can read and write from. These can be for example color and depth. Then the graphics pipeline gets created. In drawing the triangle, graphics pipeline's idea is to turn the vertex shader passing per-vertex data to the graphics pipeline and 
fragment shader determining color and depth of each fragment (separate files representing a triangle and its color) into pixels on a 2d screen. The window surface finally presents the images of the triangle to the screen interface when images are feeded from the
swap chain. Graphics pipeline gets binded to a commandbuffer and the triangle gets drawn.

*/


